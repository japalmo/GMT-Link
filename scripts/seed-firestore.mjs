function buildDashboardRollup() {
  const pendingApprovalCount = reimbursements.filter((item) => item.status === 'pending_approval').length;
  const approvedUnpaid = reimbursements.filter((item) => item.status === 'approved' && item.paymentStatus === 'unpaid');
  const paidThisMonth = reimbursements
    .filter((item) => item.status === 'paid')
    .reduce((sum, item) => sum + item.amount, 0);

  return {
    pendingCount: pendingApprovalCount,
    approvedUnpaidCount: approvedUnpaid.length,
    approvedUnpaidAmount: approvedUnpaid.reduce((sum, item) => sum + item.amount, 0),
    paidThisMonthAmount: paidThisMonth,
    paidThisMonthCount: reimbursements.filter((item) => item.status === 'paid').length,
    totalWorkersActive: workers.filter((item) => item.active).length,
    lastUpdated: ts('2026-04-20T17:35:00-04:00'),
    // Compatibility aliases while views migrate to the new schema fields.
    requestsThisMonth: reimbursements.length,
    pendingApprovalCount,
    amountToPay: approvedUnpaid.reduce((sum, item) => sum + item.amount, 0),
    paidThisMonth,
    source: 'demo_seed',
  };
}

async function seedCollection(batch, collectionName, items) {
  items.forEach((item) => {
    batch.set(doc(db, collectionName, item.id), {
      ...item,
      source: 'demo_seed',
      updatedAt: ts('2026-04-20T17:35:00-04:00'),
    });
  });
}

async function run() {
  const batch = writeBatch(db);

  await seedCollection(batch, 'users', users);
  await seedCollection(batch, 'workers', workers);
  await seedCollection(batch, 'reimbursements', reimbursements);
  await seedCollection(batch, 'paymentBatches', paymentBatches);

  batch.set(doc(db, 'dashboardRollups', 'current'), buildDashboardRollup());
  batch.set(doc(db, 'seedMeta', 'demoSeed'), {
    seededAt: ts('2026-04-20T17:35:00-04:00'),
    projectId: firebaseConfig.projectId,
    collections: {
      users: users.length,
      workers: workers.length,
      reimbursements: reimbursements.length,
      paymentBatches: paymentBatches.length,
    },
    schemaVersion: '1.0',
    source: 'demo_seed',
  });

  await batch.commit();

  console.log(`Seed completed for project ${firebaseConfig.projectId}.`);
  console.log(`users: ${users.length}`);
  console.log(`workers: ${workers.length}`);
  console.log(`reimbursements: ${reimbursements.length}`);
  console.log(`paymentBatches: ${paymentBatches.length}`);
  console.log('dashboardRollups/current updated.');
}

run().catch((error) => {
  console.error('Seed failed.');
  console.error(error);
  process.exitCode = 1;
});

